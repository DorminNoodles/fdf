#include "fdf.h"

void	free_map(t_ml *dna)
{
	int i;

	while (i < dna->map_w)
	{
		free(dna->map[i]);
		dna->map[i] = NULL;
		i++;
	}
	free(dna->map);
	dna->map = NULL;
}
